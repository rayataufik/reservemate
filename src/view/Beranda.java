    public void setLabelTotalharga(){   
        Date tanggalMasuk = this.tanggalMasuk.getDate();
        Date tanggalKeluar = this.tanggalKeluar.getDate();

        if (tanggalMasuk != null && tanggalKeluar != null) {
            long selisihMillis = tanggalKeluar.getTime() - tanggalMasuk.getTime();
            int selisihHari = (int) (selisihMillis / (24 * 60 * 60 * 1000));

            String tipeKamarTerpilih = tipeKamar.getSelectedItem().toString();
            int harga = 0;

            if (tipeKamarTerpilih.equals("Standar")) {
                harga = 100000;
            } else if (tipeKamarTerpilih.equals("VIP")) {
                harga = 200000;
            } else if (tipeKamarTerpilih.equals("Superior")) {
                harga = 300000;
            } else if (tipeKamarTerpilih.equals("Deluxe")) {
                harga = 400000;
            }

            int totalHarga = harga * selisihHari;
            totalHargaLabel.setText(Integer.toString(totalHarga));

        }
    }
    
