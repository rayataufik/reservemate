    private void tabelPelangganMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelPelangganMouseClicked
        int selectedRow = tabelPelanggan.getSelectedRow();
        // Make sure a row is selected
        if (selectedRow != -1) {
            tableModelReservasi model = (tableModelReservasi) tabelPelanggan.getModel();

            // Assuming id is at index 0
            this.id = (int) model.getValueAt(selectedRow, 0);
            
            namaPelanggan.setText((String) model.getValueAt(selectedRow, 1));
            nohp.setText((String) model.getValueAt(selectedRow, 2));
            jumlahPelanggan.setText(String.valueOf(model.getValueAt(selectedRow, 3)));
            tipeKamar.setSelectedItem((String) model.getValueAt(selectedRow, 4));
            noKamar.setText(String.valueOf(model.getValueAt(selectedRow, 5)));
            String dateString = (String) model.getValueAt(selectedRow, 6);
            String dateString1 = (String) model.getValueAt(selectedRow, 7);
            totalHargaLabel.setText(String.valueOf(model.getValueAt(selectedRow, 8)));
            tipePembayaran.setSelectedItem((String) model.getValueAt(selectedRow, 9));
            try {
                SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
                Date parsedDate = dateFormat.parse(dateString);
                Date parsedDate1 = dateFormat.parse(dateString1);
                tanggalMasuk.setDate(parsedDate);
                tanggalKeluar.setDate(parsedDate1);
            } catch (ParseException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_tabelPelangganMouseClicked

